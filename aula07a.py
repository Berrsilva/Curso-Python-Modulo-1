nome = input('Qual é o seu nome? ')
print('Prazer em te conhecer {:^20}!'.format(nome))
# Podemos usar o "{:20}!" para a exclamação ficar mais a direita.
# Ex: Prazer em te conhecer Bernardo             !
# Se botarmos o "{:>20}!", ele ficara também com o nome mais a direita.
# Ex: Prazer em te conhecer               Bernardo!
# Também podemos "{:<20}!", para alinhar a esquerda.
# Se usarmos "{:^20}!", ele ficara alinhado.

